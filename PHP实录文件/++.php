<?php

	$a = 1;
	$b = 1;
	
	$a1 = $a++;  // $a++是$a++ = 先把$a赋值给新的变量，然后++
	$b1 = ++$b; // ++$b是++$b = 先++，再赋值
	
	/*
	 *   $a ++   = {
	 *         $a1 = $a;
	 * 		$a = $a + 1;  
	 *     }
	 * 
	 *	++$b= {
	 *       	$b = $b + 1;
	 * 		$b1 = $b;
	 *    }
	 *  
	 * 
	 * 
	 * 
	 */
	
	echo "\$a1 = ".$a1." | \$a = $a<br/>";
	echo "\$b1 = ".$b1." | \$b = $b<br/>";

?>
